<script>
import { mapActions, mapGetters } from "vuex";
import "./PlayerControl.css";

import PlayIcon from "../icon/PlayIcon.vue";
import PauseIcon from "../icon/PauseIcon.vue";
import RepeatIcon from "../icon/RepeatIcon.vue";
import StopIcon from "../icon/StopIcon.vue";

import {
  ACTIVE_STATE,
  REST_STATE,
  READY_STATE,
} from "../../constants/constants";

export default {
  name: "PlayerControl",
  components: {
    PlayIcon,
    PauseIcon,
    RepeatIcon,
    StopIcon,
  },
  data() {
    return {
      colors: {
        [ACTIVE_STATE]: "#ff7373",
        [REST_STATE]: "#6ac5f9",
        [READY_STATE]: "#000000",
      },
    };
  },
  computed: {
    ...mapGetters(["currentRun", "currentRunningTimer", "selectedTimerID"]),
    isActive() {
      return this.currentRun.isActive;
    },
    isPaused() {
      return this.currentRun.isPaused;
    },
    currentRunState() {
      return this.currentRun.state;
    },
    currentColor() {
      return this.currentRunningTimer.isInterval
        ? this.colors[this.currentRunState]
        : this.colors[READY_STATE];
    },
    borderStyle() {
      return `border-color:${this.currentColor}`;
    },
  },
  methods: {
    ...mapActions(["togglePause", "toggleTimer", "toggleWorkoutStarted"]),
    stopTimer() {
      this.toggleTimer(this.selectedTimerID);
      this.toggleWorkoutStarted();
    },
    rewindTimer() {
      this.$emit("rewind");
    },
  },
};
</script>

<template>
  <div class="player-root" :style="borderStyle">
    <RepeatIcon :on-click="rewindTimer" :color="currentColor" />
    <PlayIcon v-if="isPaused" :on-click="togglePause" :color="currentColor" />
    <PauseIcon v-else :on-click="togglePause" :color="currentColor" />
    <StopIcon :on-click="stopTimer" :color="currentColor" />
  </div>
</template>
